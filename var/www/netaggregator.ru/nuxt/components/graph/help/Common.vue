<template>
    <v-layout class="pane scroller" :class="`theme--${$vuetify.theme.dark ? 'dark' : 'light'}`">
        <v-container class="pane__topic" :class="{ fullscreen, mobile }">
            <h2 class="pane__topic-title mb-5">Раздел общей справки графического редактора (дизайнера).</h2>

            <p class="pane__topic-definition">
                В данном справочном руководстве, рассмотрен функционал приложения "Дизайнер", предоставляя сквозную
                инструкцию по выполнению операций над изображением. И призвано помочь пользователям, получить
                представление о системе в целом. Работа с остальными модулями приложения Netaggregator, рассматривается
