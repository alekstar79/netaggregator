            <p class="pane__topic-disclosure">
                Дизайнер - это онлайн приложение, графический редактор общего назначения, включенный в состав модуля
                "Динамическая обложка" приложения Netaggregator. Отличительной чертой редактора, а равно как и всего
                приложения в целом, является акцент на визуализации, в отличии от остальных онлайн сервисов, с большими
                и сложными меню. Интерфейс программы представлен рабочей областью, в рамках которой располагается холст,
                и доступно контекстное меню быстрого доступа к некоторым функциям. А <i>drag-and-drop feature</i>, позволяет
                подгружать изображения на холст, простым перетаскиванием последних, в окно программы. В верхней части окна
                приложения, располагается главное меню программы, включающее в себя, весь основной функционал. В нижней части окна,
                располагается панель доступа к изображениям, компонентам и виджетам, применяемые на холсте, в рамках документа
                приложения. Боковые док-панели инструментов, предоставляют быстрый доступ к определенному функционалу приложения,
                тем самым обеспечивая удобство в работе. Все меню и панели, дабы обеспечить максимально свободным, рабочее пространство,
                выполнены плавающими и прячутся по краям окна браузера. Док-панели, дополнительно позволяют откреплять
                их и располагать, в произвольном, удобном месте на рабочем столе приложения. Холст, в свою очередь,
                позволяет простыми манипуляциями мыши, перетаскивание (<i>shift + left clk</i>) и прокрутка колесиком,
                панорамировать и масштабировать документ приложения. Так же, в него интегрировано собственное контекстное меню,
                открывающее быстрый доступ к своим функциям. Холст, оснащен линейками, позволяющими устанавливать направляющие
                (<i>identical photoshop functionality</i>) и управлять ими через меню линеек. Каждая сущность документа
                (изображение, фигура, текст, виджет и т.д.), имеет собственное меню настроек, доступ к которому, осуществляется
                кликом по иконке шестеренки в поле данной сущности или через менеджер слоев. В дизайнер интегрирован сервис
                стоковых фотографий - Pixabay, облегчающий доступ к графическим ресурсам и снимающий тем самым вопрос самостоятельного
                поиска последних, в сети интернет. В целом, мы постарались создать приложение, которое позволило бы абстрагироваться
                от технических аспектов и сконцентрироваться на творчестве. Для этого мы, с одной стороны, попытались максимально
                близко повторить логику и поведение отдельных компонентов и элементов интерфейса, аналогичным элементам в известных
                графических редакторах, с другой стороны - выдержать дизайн и поведение интерфейса, согласно принятым
                в вебе нормативам. Детальное рассмотрение компонентов редактора, следует далее по справке.
            </p>

            <div class="clearfix" />

            <p class="pane__topic-closing">
                Справочное руководство может быть обновлено, уточнено и дополнено.
