    <v-layout class="pane scroller" :class="`theme--${$vuetify.theme.dark ? 'dark' : 'light'}`">
        <v-container class="pane__topic" :class="{ fullscreen, mobile }">
            <h2 class="pane__topic-title mb-5">Виджет "Текст".</h2>

            <!--<p class="pane__topic-definition">
                Виджет "Текст"...
            </p>-->

            <v-img class="pane__images image-01 right" @click="view(0)" :src="images[0]" />

            <p class="pane__topic-disclosure">
                Виджет "Текст" позволяет сгенерировать код, встраивающий текстовый блок в группу пользователя.
                В интерфейсе модуля предусмотрены три поля ввода - заголовок, основной текст, описание. А
                так же всплывающее окно, для ссылки в футере блока (опционально). Функционал настройки и
                кастомизации блока рассмотрен в общем для всех виджетов разделе справочного руководства.
            </p>

            <!--<div class="clearfix" />-->

            <p class="pane__topic-closing">
